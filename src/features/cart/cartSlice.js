// note: very important format 👇🏻
// accept argument first(pizzaId in our case here), then return function as nothing is recieved
export const getCurrentQuantityById = (pizzaId) => (state) =>
  state.cart.cart.find((item) => item.pizzaId === pizzaId)?.quantity ?? 0;
